"""v2.24.1 — LU-AMEND-01 wave-3: bigserial sequence column on audit_events.

Revision ID: 0014
Revises: 0013
Create Date: 2026-05-25

Rationale (LU-AMEND-01 wave-3 specification):
    Wave-2 (migration 0011) established the hash chain (prev_hash + event_hash).
    However, the chain-row ordering used (created_at, id) is non-deterministic
    under timestamp collision (two events inserted at the same microsecond yield
    UUID v4 sort order, which is random per-replica). This weakens the hash-
    chain integrity guarantee under high-throughput: checkpoint computation on
    replica A may disagree with replica B.

    This migration closes the gap by adding a BIGSERIAL ``seq`` column that
    provides a strictly monotonic, database-assigned ordering key. The seq
    column becomes the AUTHORITATIVE ordering for:
      - prev_hash lookup in AuditChainService.compute_hashes_for_event()
      - daily merkle-root checkpoint in AuditChainService.run_daily_checkpoint()

    All future INSERTs automatically receive a unique seq (BIGSERIAL default).
    Existing rows (seq IS NULL) are backfilled in a single UPDATE ordered by
    (created_at, id) — preserving the ordering that was used in wave-2 so that
    existing chain verification remains valid.

    Compliance mapping:
      ASVS V7.3.3  — audit log integrity (tamper-evident)
      NIST AU-9    — protection of audit information
      NIST AU-10   — non-repudiation (deterministic chain ordering)
      SOC 2 CC7.2  — monitoring + evaluation of security events

Design notes:
  - BIGSERIAL = BIGINT + explicit sequence; PostgreSQL-specific. MySQL
    equivalent would be AUTO_INCREMENT BIGINT. For now, Yashigani is single-PG.
  - seq NOT NULL; no UNIQUE constraint at the DB level — audit_events is
    PARTITION BY RANGE (created_at); PG requires UNIQUE constraints on
    partitioned tables to include all partitioning columns (i.e., UNIQUE
    would need to be (seq, created_at)). The sequence object itself guarantees
    monotonic non-repeating values; we document this as an accepted limitation.
  - BIGSERIAL sequences are NOT transactionally rolled back on INSERT error —
    a failed INSERT advances the sequence (gap-safe). Chain integrity relies on
    strict monotonic order, not on contiguous seq values.
  - Backfill uses a CTE to assign deterministic seq values via ROW_NUMBER()
    ordered by (created_at, id) for NULL-seq rows. This preserves wave-2 ordering.
  - Forward-only: no downgrade path (sequences are append-only; reversing the
    column would leave orphaned seq references in the chain). Downgrade raises.

Evidence artefact: reference this migration SHA in the v2.24.1 compliance pack
for LU-AMEND-01 wave-3 closure.

Last updated: 2026-05-25T00:00:00+00:00
"""
